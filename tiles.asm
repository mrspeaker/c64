                sta $d800+$ff,x
                lda map_color_data+$ff*2,x
                sta $d800+$ff*2,x
                lda map_color_data+$ff*3,x
                sta $d800+$ff*3,x
                dex
                bne !-

                rts

copyChars:
                lda $d018
                and #%11110001
                ora #%00001010
                sta $d018               // locate chars at $1000

                lda #<$2800
                sta $fb
                lda #>$2800
                sta $fb+1
                ldy #$00
_copyFF:
                lda charset_data,y
                sta ($fb),y
                iny
                bne _copyFF

                inc $fb+1

_copyFF2:
                lda charset_data+$ff,y
                sta ($fb),y
                iny
                bne _copyFF2
                rts

init_sprites:
                lda #%00000011
                sta $d015
                sta $d01b
                lda $d01e

                ldx #64*2
!:
                lda spr_bg,x
                sta $340,x
                dex
                bpl !-

                lda #$340/64
                sta $7f8
                lda #$340/64+1
                sta $7f9

                lda #$b1
                sta $d002
                sta $d003
                lda #$50
                sta $d000
                sta $d001

                lda #1
                sta $d028

                rts

update:
                inc $d003

                lda player_x
                ldx player_y

                clc
                adc player_dir
                tay
                bcc noMSB
                // Set MSB
                lda $d010
                eor #$1
                sta $d010
noMSB:
                lda $d010
                lsr
                bcc noWrap

                tya
                cmp #80
                bmi noWrap
                lda $d010
                eor #$1
                sta $d010
                ldy #0
noWrap:
                tya
                sta player_x
                sta $d000
                stx $d001

                lda $dc00 // bits: 0=up,1=down,2=left,3=right,4=fire
                lsr       // if bit 0 was 0 (active low), it's rotated to carry
                bcs down  // so carry will be clear if pressed
                dec player_y
down:
                lsr
                bcs left
                inc player_y
left:
                lsr
                bcs right
right:
                lsr
                bcs fire
fire:
                lsr
                bcs !+
                jsr hit
!:
                rts

hit:
                 // Hit!
                //dec $d020
                inc score
                lda score
                sta $d028

                // repos baloon
                lda $dc04
                eor $dc05
                sta $d003
                rts

irq:
                dec $d019
                jsr update
                 // Check collision
                lda $d01e
                and #%00000001
                beq !+
                jsr hit
!:

                pla
                tay
                pla
                tax
                pla
                rti

                rti

